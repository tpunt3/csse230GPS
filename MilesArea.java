import javax.swing.JTextArea;
import javax.swing.text.Document;


public class MilesArea extends JTextArea {

	public MilesArea() {
		super();
	}

	public MilesArea(String arg0) {
		super(arg0);
	}

	public MilesArea(Document arg0) {
		super(arg0);
	}

	public MilesArea(int arg0, int arg1) {
		super(arg0, arg1);
	}

	public MilesArea(String arg0, int arg1, int arg2) {
		super(arg0, arg1, arg2);
	}

	public MilesArea(Document arg0, String arg1, int arg2, int arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
